#!/usr/bin/env bash

set -euo pipefail

run_apt_update=1
declare -a nameservers=()
require_compute_artifactory=0

while [[ $# -gt 0 ]]; do
  case "$1" in
    --no-apt-update)
      run_apt_update=0
      shift
      ;;
    --nameserver)
      if [[ $# -lt 2 ]]; then
        echo "missing value for --nameserver" >&2
        exit 2
      fi
      nameservers+=("$2")
      shift 2
      ;;
    --require-compute-artifactory)
      require_compute_artifactory=1
      shift
      ;;
    *)
      nameservers+=("$1")
      shift
      ;;
  esac
done

if [[ "${#nameservers[@]}" -eq 0 ]]; then
  nameservers=(223.5.5.5 223.6.6.6 1.1.1.1 8.8.8.8)
fi

quoted_nameservers=()
for nameserver in "${nameservers[@]}"; do
  quoted_nameservers+=("$(printf '%q' "$nameserver")")
done

cat <<EOF
set -euo pipefail

run_apt_update="${run_apt_update}"
require_compute_artifactory="${require_compute_artifactory}"
required_hosts=(archive.ubuntu.com security.ubuntu.com github.com)
if [[ "\${require_compute_artifactory}" == "1" ]]; then
  required_hosts+=(compute-artifactory.amd.com)
fi
candidate_nameservers=(${quoted_nameservers[*]})
resolver_path="/etc/resolv.conf"
backup_path="/tmp/paddle_amd_resolv.conf.backup"

collect_current_nameservers() {
  awk '/^[[:space:]]*nameserver[[:space:]]+/ {print \$2}' "\${resolver_path}" 2>/dev/null || true
}

unique_nameservers() {
  awk '!seen[\$0]++'
}

resolve_host() {
  local host="\$1"
  getent ahostsv4 "\$host" >/dev/null 2>&1 || getent hosts "\$host" >/dev/null 2>&1
}

dns_ok() {
  local host
  for host in "\${required_hosts[@]}"; do
    if ! resolve_host "\$host"; then
      echo "dns lookup failed: \$host" >&2
      return 1
    fi
  done
}

print_resolver_state() {
  ls -l "\${resolver_path}" || true
  cat "\${resolver_path}" || true
}

echo "== resolver before change =="
print_resolver_state

if dns_ok; then
  echo "DNS already resolves all required hosts"
else
  echo
  echo "== applying resolver override =="
  if [[ -e "\${resolver_path}" ]]; then
    cp -L "\${resolver_path}" "\${backup_path}"
  else
    : > "\${backup_path}"
  fi

  tmp_resolver="\$(mktemp)"
  mapfile -t current_nameservers < <(collect_current_nameservers)
  mapfile -t merged_nameservers < <(
    {
      for nameserver in "\${current_nameservers[@]}"; do
        [[ -n "\${nameserver}" ]] && echo "\${nameserver}"
      done
      for nameserver in "\${candidate_nameservers[@]}"; do
        [[ -n "\${nameserver}" ]] && echo "\${nameserver}"
      done
    } | unique_nameservers
  )

  {
    echo "# Generated by paddle-amd remote DNS repair helper"
    for nameserver in "\${merged_nameservers[@]}"; do
      echo "nameserver \${nameserver}"
    done
    echo "options timeout:2 attempts:2 rotate ndots:1"
  } >"\${tmp_resolver}"

  cat "\${tmp_resolver}" >"\${resolver_path}"
  rm -f "\${tmp_resolver}"

  echo "== resolver after change =="
  print_resolver_state

  if ! dns_ok; then
    echo "DNS is still failing after resolver override; restoring previous resolver file" >&2
    cat "\${backup_path}" >"\${resolver_path}" || true
    exit 1
  fi
fi

echo
echo "== hostname resolution check =="
for host in "\${required_hosts[@]}"; do
  echo "-- \${host}"
  getent ahostsv4 "\${host}" || getent hosts "\${host}" || true
done

if [[ "\${run_apt_update}" == "1" ]]; then
  echo
  echo "== apt-get update =="
  apt-get update
fi
EOF